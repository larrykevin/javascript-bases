//Truthy and Falsy

//Hay varios tipos de valores que son falsos
  Boolean(0) // False
  Boolean(null) // False
  Boolean(undefined) // False
  Boolean(NaN) // False
  Boolean(false) // False
  Boolean("") // False

//Lista de verdaderos
  Boolean(1) // True
  Boolean("a") // True
  Boolean([]) // True
  Boolean({}) // True
  Boolean(function(){}) // True
  Boolean(true) // True